

public class Comparison_Operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println("a == b = " + (a == b) ); //False
        System.out.println("a != b = " + (a != b) ); //True
        System.out.println("a > b = " + (a > b) ); //False
        System.out.println("a < b = " + (a < b) ); //True
        System.out.println("b >= a = " + (b >= a) ); //True
        System.out.println("b <= a = " + (b <= a) ); //False
    }
}

